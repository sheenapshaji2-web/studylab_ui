// Email validation while typing
function validateEmail(){

let email = document.getElementById("email").value;
let emailError = document.getElementById("emailError");

let emailPattern = /^[a-z][a-z0-9._%+-]*@[a-z0-9.-]+\.[a-z]{2,3}$/;

if(!email.match(emailPattern)){
emailError.innerHTML = "Enter a valid email address";
return false;
}
else{
emailError.innerHTML = "";
return true;
}

}


// Website validation while typing
function validateWebsite(){

let website = document.getElementById("website").value;
let websiteError = document.getElementById("websiteError");

let websitePattern = /^(https?:\/\/).+/;

if(website != "" && !website.match(websitePattern)){
websiteError.innerHTML = "Enter valid website (example: https://example.com)";
return false;
}
else{
websiteError.innerHTML = "";
return true;
}

}


// Final form validation
function validateForm(){

